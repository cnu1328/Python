num = int(input("Enter a number to get a day : "))
if num == 7 :
    print('SUNDAY')
elif num == 1 :
    print('MONDAY')
elif num == 2 :
    print('TUESDAY')
elif num == 3 :
    print('WEDNESDAY')
elif num == 4 :
    print('THURSDAY')
elif num == 5 :
    print('FRIDAY')
elif num == 6 :
    print('SATURDAY')
else:
    print('Enter valid number')
